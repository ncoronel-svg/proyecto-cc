#hacer libreria de calculos suma, resta, division, multiplicacion, modulo


def suma(a,b):
    resultado = a + b
    return resultado

print(suma(2,3))

def resta(a,b):
    resultado = a - b
    return resultado

print(resta(3,2))

def multi(a, b):
    resultado = a * b
    return resultado

print(multi(3,2))

def div(a, b):
    resultado = a / b
    return resultado

print(div(2,3))

